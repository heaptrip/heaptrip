package com.heaptrip.web.model.travel;


public class RouteModel {

	// route description
	private String text;

	// route map
	private String map;

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getMap() {
		return map;
	}

	public void setMap(String map) {
		this.map = map;
	}

}
