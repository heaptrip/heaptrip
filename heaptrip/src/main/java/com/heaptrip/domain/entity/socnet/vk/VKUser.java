package com.heaptrip.domain.entity.socnet.vk;

public class VKUser {

	private String uid;
	private String first_name;
	private String last_name;
	private String sex;
	private String bdate;
	private String country;
	private String city;
	private String timezone;
	private String photo;
	private String photo_medium;
	private String photo_big;
	private String photo_rec;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getFirst_name() {
		return first_name;
	}

	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}

	public String getLast_name() {
		return last_name;
	}

	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getBdate() {
		return bdate;
	}

	public void setBdate(String bdate) {
		this.bdate = bdate;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getTimezone() {
		return timezone;
	}

	public void setTimezone(String timezone) {
		this.timezone = timezone;
	}

	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

	public String getPhoto_medium() {
		return photo_medium;
	}

	public void setPhoto_medium(String photo_medium) {
		this.photo_medium = photo_medium;
	}

	public String getPhoto_big() {
		return photo_big;
	}

	public void setPhoto_big(String photo_big) {
		this.photo_big = photo_big;
	}

	public String getPhoto_rec() {
		return photo_rec;
	}

	public void setPhoto_rec(String photo_rec) {
		this.photo_rec = photo_rec;
	}

}
